const input_search = document.querySelector('.header__search-input')
const input_search_history = document.querySelector('.header__search-history')
const input_searchBox = document.querySelector('.header__search-keyDown')
const searchBtn = document.querySelector('.header__search-btn')


document.addEventListener("click", (e) => {
    let targetElement = e.target;
    if (targetElement == input_search) {
        // This is a click inside. Do nothing, just return.
        input_search_history.style.display = 'block';
        return
    }
    else {
        input_search_history.style.removeProperty('display');
        input_searchBox.style.display = 'none'
    }
});

input_search.onkeyup = () => {

    let userData = input_search.value;
    let emptyArray = [];


    emptyArray = suggestions.filter((data) => data.toLocaleLowerCase().startsWith(userData.toLocaleLowerCase()))
    emptyArray = emptyArray.map((data) => data = `<li>${data.toLocaleLowerCase()}</li>`)
    render(emptyArray)


    const searchBox_item = document.querySelectorAll('.header__search-keyDown li')
    searchBox_item.forEach(item => {
        item.onclick = function () {
            this.classList.add('header__search-history-item')
            input_search_history.appendChild(this)
            input_search.value = this.innerHTML

        }
    })

    input_search_history.style.removeProperty('display');
    input_searchBox.style.display = 'block';

}

function render(list) {
    let listData;
    // console.log(list.length);
    if (!list.length) {
        userValue = input_search.value;
        listData = `<li>${userValue}</li>`
    } else {
        listData = list.join('')
    }
    input_searchBox.innerHTML = listData
}


searchBtn.onclick = function () {
    input_search.value = ''
}